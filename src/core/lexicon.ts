const POSITIVE = [
  'うれしい','嬉しい','たのしい','楽しい','よかった','良かった','すき','好き','最高','さいこう',
  'ありがとう','感謝','かんしゃ','幸せ','しあわせ','おもしろい','面白い','わくわく','ワクワク',
  'できた','がんばった','頑張った','がんばれた','成功','せいこう','達成','たっせい','うまくいった',
  'かわいい','可愛い','きれい','綺麗','すてき','素敵','癒された','いやされた','笑った','わらった',
  'ほっとした','安心','あんしん','元気','げんき','前向き','まえむき','希望','きぼう','夢','ゆめ',
  'おいしい','美味しい','さいこうだった','楽しめた','たのしめた','満足','まんぞく','ほめられた',
  'よろこんだ','喜んだ','気持ちいい','きもちいい','スッキリ','すっきり','回復','かいふく',
]

const NEGATIVE = [
  'つらい','辛い','かなしい','悲しい','しんどい','きつい','つかれた','疲れた','だるい',
  'むずかしい','難しい','うまくいかない','失敗','しっぱい','後悔','こうかい','怒','いかり',
  'いやだ','嫌だ','こわい','怖い','ふあん','不安','どうしよう','もうだめ','絶望','ぜつぼう',
  'ストレス','しんどすぎる','泣いた','ないた','ひどい','最悪','さいあく','消えたい','きえたい',
  'ひとりぼっち','孤独','こどく','淋しい','さびしい','寂しい','眠れない','ねむれない',
]

export function calcPositiveDensity(text: string): number {
  if (!text) return 0.5
  const lower = text.toLowerCase()
  let pos = 0
  let neg = 0
  for (const w of POSITIVE) if (lower.includes(w)) pos++
  for (const w of NEGATIVE) if (lower.includes(w)) neg++
  const total = pos + neg
  if (total === 0) return 0.5
  return pos / total
}
